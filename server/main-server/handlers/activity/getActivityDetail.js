/**
 * Build the 28 common fields for any activity type.
 * @param {number} actType - Activity type ID
 * @param {string} actId - Activity ID (from getActivityBrief)
 * @param {object} config - Server config (for serverOpenDate)
 * @returns {object} Common act fields object
 */
function buildCommonActFields(actType, actId, config) {
    var meta = ACTIVITY_META[actType];
    if (!meta) return null;

    var serverOpenDate = config.serverOpenDate || Date.now();
    // Align startTime to midnight UTC+8 (matching HAR: 1775199600000 = 2026-04-02T00:00:00+08:00)
    var startTime = Math.floor(serverOpenDate / MS_PER_DAY) * MS_PER_DAY;
    var endTime = startTime + (meta._durationDay * MS_PER_DAY) - 1;

    // Type 2001 has no time limit
    if (meta._noTimeLimit) {
        startTime = 0;
        endTime = 0;
    }

    // Type 2002 uses different time calculation (user-based, offset by 1 day)
    // HAR shows: _startTime: 1775286000000 (day 2 00:00 UTC+8), _endTime: +7 days
    // For now, use server-based calculation (will be refined when user time tracking is implemented)
    if (actType === 2002) {
        // HAR shows this activity uses user create time + offset, not server open date
        // For Phase 1, approximate with server open date + 1 day offset
        startTime = serverOpenDate + MS_PER_DAY;
        startTime = Math.floor(startTime / MS_PER_DAY) * MS_PER_DAY;
        endTime = startTime + (meta._durationDay * MS_PER_DAY) - 1;
    }

    return {
        _id: actId,
        _templateId: '',
        _templateName: meta._templateName,
        _name: meta._name,
        __name: meta.__name,
        _des: meta._des,
        __des: meta.__des,
        _icon: meta._icon,
        _image: meta._image,
        _displayIndex: meta._displayIndex,
        _showRed: true,
        _activityType: actType,
        _cycleType: meta._cycleType,
        _enable: true,
        _timeType: meta._timeType,
        _newUserUsing: true,
        _isloop: false,
        _loopInterval: 0,
        _startDay: 0,
        _durationDay: meta._durationDay,
        _oldUserVip: meta._oldUserVip || 0,
        _oldUserServerOpenDay: 0,
        _oldUserServerOpenDayEnd: 0,
        _oldUserOfflineDay: meta._oldUserOfflineDay || 0,
        _startTime: startTime,
        _endTime: endTime,
        _loopCount: 0,
        _loopTag: '',
        _timestamp: startTime + 1310, // Small offset from startTime (HAR: 1775199600000 → 1775199601310)
        _hideos: '',
        _limitReward: { _items: {} }
    };
}

