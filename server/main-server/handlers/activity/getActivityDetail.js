/**
 * Handler: activity/getActivityDetail
 *
 * Returns full activity detail view data + user activity state.
 * This is a READ-ONLY handler — no rewards are claimed here.
 *
 * @param {object} request - { type, action, userId, actId, cycleType, version }
 * @param {object} ctx - Handler context { db, config, logger, buildDataResponse, buildErrorResponse }
 * @returns {object} Response via buildDataResponse
 */
async function handleGetActivityDetail(request, ctx) {
    var userId = request.userId;
    var actId = request.actId;
    var cycleType = request.cycleType;

    // ─── STEP 1: Validate request ───
    ctx.logger.step(1, 3, 'Validate request', 'running');
    ctx.logger.details('request',
        ['userId', userId ? userId.substring(0, 20) + '...' : 'MISSING'],
        ['actId', actId || 'MISSING'],
        ['cycleType', String(cycleType || 'MISSING')]
    );

    if (!userId || !actId) {
        ctx.logger.step(1, 3, 'Validate request', 'fail', 'userId or actId MISSING');
        return ctx.buildErrorResponse(8);
    }
    ctx.logger.step(1, 3, 'Validate request', 'pass');

    // ─── STEP 2: Parse actId → determine actType ───
    ctx.logger.step(2, 3, 'Parse actId', 'running');
    var parsed = parseActId(actId);
    if (!parsed) {
        ctx.logger.step(2, 3, 'Parse actId', 'fail', 'Invalid actId format: ' + actId);
        return ctx.buildErrorResponse(4);
    }

    var actType = parsed.actType;
    ctx.logger.details('parsed',
        ['actType', String(actType)],
        ['cycleType', String(parsed.cycleType)]
    );

    if (!SUPPORTED_TYPES[actType]) {
        ctx.logger.step(2, 3, 'Parse actId', 'fail', 'Activity type ' + actType + ' not supported in Phase 1');
        ctx.logger.details('supported', ['types', Object.keys(SUPPORTED_TYPES).join(', ')]);
        return ctx.buildErrorResponse(4);
    }
    ctx.logger.step(2, 3, 'Parse actId', 'pass');

    // ─── STEP 3: Build act (activity config) ───
    ctx.logger.step(3, 3, 'Build act config', 'running');

    var commonAct = buildCommonActFields(actType, actId, ctx.config);
    if (!commonAct) {
        ctx.logger.step(3, 3, 'Build act config', 'fail', 'No metadata for type ' + actType);
        return ctx.buildErrorResponse(4);
    }

    var typeSpecific = buildTypeSpecificActFields(actType);
    if (!typeSpecific) {
        ctx.logger.step(3, 3, 'Build act config', 'fail', 'Cannot build type-specific fields for ' + actType);
        return ctx.buildErrorResponse(4);
    }

    // Merge common + type-specific fields
    var act = Object.assign({}, commonAct, typeSpecific);

    ctx.logger.step(3, 3, 'Build act config', 'pass');
    ctx.logger.details('act',
        ['activityType', String(actType)],
        ['name', act._name],
        ['startTime', new Date(act._startTime).toISOString()],
        ['endTime', new Date(act._endTime).toISOString()]
    );

    // ─── STEP 4: Build/Load uact (user activity state) ───
    ctx.logger.step(4, 3, 'Build uact', 'running');

    // Try to load existing uact from DB
    var uact = loadUact(ctx.db, userId, actId);

    if (uact) {
        // Existing user state found — update _haveClick
        uact._haveClick = true;
        ctx.logger.details('uact', ['source', 'loaded from DB']);
    } else {
        // Fresh state for new user/activity
        uact = buildFreshUact(actType, actId, commonAct);
        ctx.logger.details('uact', ['source', 'fresh (new)']);
    }

    // Save uact back to DB
    saveUact(ctx.db, userId, actId, uact);

    ctx.logger.step(4, 3, 'Build uact', 'pass');

    // ─── STEP 5: Build response ───
    var responseData = {
        type: request.type || 'activity',
        action: request.action || 'getActivityDetail',
        userId: userId,
        actId: actId,
        cycleType: cycleType || parsed.cycleType,
        version: request.version || '1.0',
        act: act,
        uact: uact,
        forceEndTime: 0
    };

    ctx.logger.details('response',
        ['actType', String(actType)],
        ['actFields', String(Object.keys(act).length)],
        ['uactFields', String(Object.keys(uact).length)]
    );

    return ctx.buildDataResponse(0, responseData);
}

