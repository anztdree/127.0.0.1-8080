// ═══════════════════════════════════════════════════════════════
// UACT PERSISTENCE — Load / Save user activity state
// ═══════════════════════════════════════════════════════════════

/**
 * Load user activity state from DB.
 * Stored under userData._activityState[actId].
 * @param {object} db - Database module
 * @param {string} userId - User ID
 * @param {string} actId - Activity ID
 * @returns {object|null} Saved uact, or null if not found
 */
function loadUact(db, userId, actId) {
    try {
        var userData = db.getUser(userId);
        if (!userData || !userData._activityState) return null;
        return userData._activityState[actId] || null;
    } catch (e) {
        return null;
    }
}

/**
 * Save user activity state to DB.
 * @param {object} db - Database module
 * @param {string} userId - User ID
 * @param {string} actId - Activity ID
 * @param {object} uact - User activity state to save
 */
function saveUact(db, userId, actId, uact) {
    try {
        var userData = db.getUser(userId);
        if (!userData) return false;
        if (!userData._activityState) {
            userData._activityState = {};
        }
        userData._activityState[actId] = uact;
        db.setUser(userId, userData);
        return true;
    } catch (e) {
        return false;
    }
}

// ═══════════════════════════════════════════════════════════════
// PARSE ACT ID — Extract actType from act_{actType}_{cycleType}
// ═══════════════════════════════════════════════════════════════

/**
 * Parse actId format: act_{actType}_{cycleType}
 * @param {string} actId - Activity ID from request
 * @returns {{ actType: number, cycleType: number }|null}
 */
function parseActId(actId) {
    if (!actId || typeof actId !== 'string') return null;
    var parts = actId.split('_');
    if (parts.length !== 3 || parts[0] !== 'act') return null;
    var actType = parseInt(parts[1], 10);
    var cycleType = parseInt(parts[2], 10);
    if (isNaN(actType) || isNaN(cycleType)) return null;
    return { actType: actType, cycleType: cycleType };
}

